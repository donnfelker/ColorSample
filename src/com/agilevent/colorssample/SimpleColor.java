package com.agilevent.colorssample;

public class SimpleColor {
	
	String name; 
	int color; 
	
	public SimpleColor(String name, int color) {
		this.name = name;
		this.color = color; 
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}
	
	
	
}
